# Payloads designed to trigger WAF responses
# These look like common attack patterns WAFs are trained to block
# We're not actually attacking — just probing WAF behavior
PROBE_PAYLOADS = [
    "?id=1'%20OR%20'1'='1",                          # SQLi
    "?q=<script>alert(1)</script>",                   # XSS
    "?file=../../../etc/passwd",                      # Path traversal
    "?cmd=;ls%20-la",                                 # Command injection
    "?search=UNION%20SELECT%201,2,3--",               # SQLi UNION
]

# Status codes that suggest WAF blocking
BLOCK_CODES = {403, 406, 419, 429, 501, 502, 503}

